// ───────────────────────────────────────────────────────────────────────
// Meta-sweep item 4 — generate / verify chat-turn's deploy fingerprint.
//
//   node scripts/gen-edge-fingerprint.mjs           → (re)write the file
//   node scripts/gen-edge-fingerprint.mjs --check   → exit 1 on drift
//
// --check is wired into prebuild (verify:edge-fingerprint) so the
// generated deployFingerprint.ts can never go stale on main — a stale
// fingerprint would make the verify:deploy edge rail lie, which is the
// exact gate-rot class this sweep is closing.
// ───────────────────────────────────────────────────────────────────────

import { readFileSync, writeFileSync, existsSync } from 'node:fs'
import { dirname, join } from 'node:path'
import { fileURLToPath } from 'node:url'
import {
  computeEdgeFingerprint,
  FINGERPRINT_FILE,
} from './lib/edgeFingerprint.mjs'

const repoRoot = join(dirname(fileURLToPath(import.meta.url)), '..')
const target = join(repoRoot, FINGERPRINT_FILE)
const { fingerprint, files } = computeEdgeFingerprint(repoRoot)

const content = `// AUTO-GENERATED by scripts/gen-edge-fingerprint.mjs — DO NOT EDIT.
// sha256 (first 12 hex) over the chat-turn import graph (${files.length} files,
// walked from index.ts incl. the ../../../src legal/type modules the deploy
// bundler ships). Served by the GET health probe; compared against the local
// tree by \`npm run verify:deploy\` (edge rail). Regenerate:
//   node scripts/gen-edge-fingerprint.mjs
export const EDGE_FINGERPRINT = '${fingerprint}'
export const EDGE_FINGERPRINT_FILES = ${files.length}
`

if (process.argv.includes('--check')) {
  const current = existsSync(target) ? readFileSync(target, 'utf-8') : ''
  if (current !== content) {
    console.error(
      `[verify:edge-fingerprint] FAIL — ${FINGERPRINT_FILE} is stale ` +
        `(expected fingerprint ${fingerprint} over ${files.length} graph files).\n` +
        '  The chat-turn import graph changed without regenerating the deploy\n' +
        '  fingerprint. Run: node scripts/gen-edge-fingerprint.mjs',
    )
    process.exit(1)
  }
  console.log(
    `[verify:edge-fingerprint] OK — ${fingerprint} (${files.length} graph files)`,
  )
} else {
  writeFileSync(target, content)
  console.log(
    `[gen-edge-fingerprint] wrote ${FINGERPRINT_FILE} — ${fingerprint} (${files.length} graph files)`,
  )
}
